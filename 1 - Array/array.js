let lista = [] // Declara uma variável chamada "lista" e a inicializa como um array vazio.

// Adiciona elementos ao array "lista" em posições específicas.
lista[0] = "elemento1" // Atribui a string "elemento1" à posição 0 do array.
lista[1] = "elemento2" // Atribui a string "elemento2" à posição 1 do array.
lista[2] = 200 // Atribui o número 200 à posição 2 do array.
lista[3] = true // Atribui o valor booleano "true" à posição 3 do array.

console.log(lista) // Exibe todo o conteúdo do array "lista" no console.
// Saída esperada: ["elemento1", "elemento2", 200, true]

console.log(lista[3]) // Exibe o valor armazenado na posição 3 do array "lista" no console.
// Saída esperada: true
